// If e Else JS

const imprimirResultadoNota = function(nota) {
    if(nota >= 7) {
        console.log(`Parabéns! Você foi aprovado!`)
    } else {
        console.log(`Infelizmente você foi reprovado!`)
    }
}

imprimirResultadoNota(4) //Agora estamos invocando chamando a função passando para ela o parametro 4.
imprimirResultadoNota(10) //Agora estamos invocando chamando a função passando para ela o parametro 10.

